namespace EjemplosWwhile
{
    internal class Program
    {
        static void Main(string[] args)
        {
            int repetir = 100;
            int i = 100; //este es un contador 

            while (i < repetir)
            {
                i++;
                Console.WriteLine("Bienvenido  i =" + 1);
            }
            do
            {
                i++;
                Console.WriteLine("Biemvenido  i=" + i)

             } while (i < repetir);



        }
    }
}
